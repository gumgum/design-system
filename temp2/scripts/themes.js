// Utility to make light and dark variations of colors from colorPalette
function makeColorVariation(name, direction, amount) { // '#00a7cf', [light | dark], [1, 2, 3, 4]
    let directionValue = direction === 'Light' ? 'white' : 'black',
        amountValue,
        stringOutput;

    if (amount === 1) {
        amountValue = 5;
    } else if (amount === 2) {
        amountValue = 10;
    } else if (amount === 3) {
        amountValue = 15;
    } else if (amount === 4) {
        amountValue = 30;
    } else if (amount === 5) {
        amountValue = 50;
    }

    stringOutput = `mix(${directionValue},$${name}Color,${amountValue}%)`;
    return stringOutput;
}
